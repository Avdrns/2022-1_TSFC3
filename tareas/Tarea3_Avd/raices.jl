function INR_method(f, I::Intervalo, tol)
    0.0 ∉ f(I) && return false
	roots = [I]
    (I.supremo - I.infimo < tol) && return roots
    counter = 0   ### counter for enter the first time in the while cycle
    while counter < length(roots)
        counter = 0   ### counter used for now on to keep cycling
        more_roots = [Intervalo(0)]   ### where it's gonna be stored the new root(s)
        for Ir in roots
            if Ir.supremo - Ir.infimo < tol  ### Checking diameter smaller than tol
                counter += 1
				push!(more_roots, Ir)
            else   ### Interval Newton-Rhapson method
                mp = Ir.infimo + (Ir.supremo - Ir.infimo)*0.5   ### mid-point
                mIr = Intervalo(mp)   ### mid-Interval
                F′ = ForwardDiff.derivative(f, Ir)
                Intervals = mIr .- division_extendida(f(mIr), F′)   ### recursive
                for Is in Intervals
                    intersect = Is ∩ Ir
                    0.0 ∈ f(intersect) && push!(more_roots, intersect)
                end
            end
        end
		popfirst!(more_roots)
        counter == length(more_roots) && return more_roots
        roots = more_roots   ### If not, redefine the interval solutions array
    end
end

