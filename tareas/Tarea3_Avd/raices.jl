# ╔═╡ 3090f4f9-130d-4ec1-8689-fc9492893e2c
function esmonotona(f, I)
	Deriv = ForwardDiff.derivative(f, I)
	if 0.0 ∈ Deriv
		return false
	else
		return true
	end
end

# ╔═╡ ab894a96-c403-449c-b45c-b15eae978b1d
function unicity(f, V, tol)
	l = length(V)
	uni = trues(l)
	for I in 1:l
		monot = esmonotona(f, V[I])
		if monot == false
			uni[I] = false
		end
	end
	return uni
end

# ╔═╡ 3d099169-4f12-4144-8073-898c7714f204
function ceros_newton(f, I::Intervalo, tol=10e-3)
	roots = INR_method(f, I, tol)
	unis = unicity(f, roots, tol)
	return Raiz.(roots, unis)
end

